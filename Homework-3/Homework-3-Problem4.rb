# Two common methods for a request-response between a client and server are:

# GET - Requests data from a specified resource
# POST - Submits data to be processed to a specified resource

# # ***************************************************************************************************

# # ************************************** Compare GET vs. POST ***************************************

# 										GET							POST
# Bookmarked						Can be bookmarked			Cannot be bookmarked

# Cached							  Can be cached					Not cached

# History							Parameters remain		Parameters are not saved
# 								in browser history			in browser history

# Restrictions on data length				Yes							No

# Restrictions on data type		Only ASCII characters allowed	  No restrictions.
# 															Binary data is also allowed





